"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { useState } from "react";

const navItems = [
  { label: "Dashboard", href: "/", icon: HomeIcon },
  { label: "Leads", href: "/leads", icon: UsersIcon },
  { label: "Map", href: "/map", icon: MapIcon },
  { label: "Lead Finder", href: "/lead-finder", icon: SearchIcon },
];

export default function Sidebar() {
  const pathname = usePathname();
  const [open, setOpen] = useState(false);

  return (
    <>
      <button
        onClick={() => setOpen(!open)}
        className="fixed top-4 left-4 z-50 rounded-md bg-zinc-900 p-2 text-white lg:hidden"
        aria-label="Toggle sidebar"
      >
        <HamburgerIcon />
      </button>

      {open && (
        <div
          className="fixed inset-0 z-30 bg-black/50 lg:hidden"
          onClick={() => setOpen(false)}
        />
      )}

      <aside
        className={`fixed inset-y-0 left-0 z-40 flex w-64 flex-col bg-zinc-900 text-zinc-100 transition-transform duration-200 lg:translate-x-0 ${
          open ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        <div className="flex h-16 items-center gap-2 px-6 border-b border-zinc-800">
          <span className="text-lg font-bold tracking-tight">CRM App</span>
        </div>

        <nav className="flex-1 space-y-1 px-3 py-4">
          {navItems.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={() => setOpen(false)}
                className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                  isActive
                    ? "bg-zinc-800 text-white"
                    : "text-zinc-400 hover:bg-zinc-800 hover:text-white"
                }`}
              >
                <item.icon className="h-5 w-5" />
                {item.label}
              </Link>
            );
          })}
        </nav>

        <div className="border-t border-zinc-800 px-3 py-4">
          <div className="flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm text-zinc-400">
            <div className="h-8 w-8 rounded-full bg-zinc-700" />
            <div>
              <p className="text-sm font-medium text-white">Admin</p>
              <p className="text-xs text-zinc-500">admin@example.com</p>
            </div>
          </div>
        </div>
      </aside>
    </>
  );
}

